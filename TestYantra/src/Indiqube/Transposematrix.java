package Indiqube;

public class Transposematrix 
{
public static void main(String[] args)
{
	int[][]a= {{1,2},{2,3}};
//	int [][]b= {{4,7},{9,8}};
	for(int i=0;i<a.length;i++)
	{
		for(int j=0;j<a[i].length;j++)
		{
			
			
				System.out.print(a[j][i]+" ");
			
		}
		System.out.println();
	}
	
	
	
	
}
}
